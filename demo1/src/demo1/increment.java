package demo1;

public class increment {
	public static void main(String[] args) {
		
		int a=20;
		int b=30;
		int c=40;
		int d=50;
		System.out.println("post increment" + a++);
	     
		System.out.println("pre increment" + ++b);
		 
		System.out.println("post decrement" + c--);
		
		System.out.println("post increment" + --d);
	}

}
