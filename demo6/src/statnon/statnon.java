package statnon;

public class statnon 
{
    public int k=44;
    int d=32;
    private int b =13;
    protected int m=56;
    
    public static void main(String[] args) 
    {    statnon x=new statnon();
        
		System.out.println(x.k);
		System.out.println(x.b);
		System.out.println(x.d);
		System.out.println(x.m);
		
		System.out.println();
	}
    
}
